"""
Create a function called power that takes two numeric values as arguments.
It must return the number that results from solving a power, using the
first number as the base, and the second as the exponent:

3^4 = 81
base = 3 (first argument)
exponent = 4 (second argument)
power = 81 (result)

===========================================================================

Crea una función llamada potencia que tome dos valores numéricos como
argumentos. Deberá devolver el número que resulte de resolver una potencia,
utilizando el primer número como base, y el segundo como exponente:

3^4 = 81
base = 3 (primer argumento)
exponente = 4 (segundo argumento)
potencia = 81 (resultado)
"""

def power(base, exponent):
    return base ** exponent

def potencia(base, exponente):
    return base ** exponente